##2. Define una función llamada "inverso_palabra" que reciba una cadena como parámetro y devuelva la cadena invertida. Por ejemplo, si la entrada es "python", la salida debería ser "nohtyp".
def inverso_palabra(cadena):##se define el nombre que se utilizará para invertir las palabras
    return cadena[::-1]##debe de retornar la cadena que utilizamos con -1 así como en las operaciones matemáticas como vector
entrada = "python"##es el nombre que debemos de invertir
salida = inverso_palabra(entrada)##el output de la palabra inversa
print("La cadena invertida es:", salida)##finalmente se imprime la cadena 












